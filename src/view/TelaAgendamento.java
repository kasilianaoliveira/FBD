public void preencherMedicos(){
    conecte.conexao();
    conecte.executaSql("select nome from medico order by nome");
    try{
        conecte.rs.first();
        jComboBoxMedicos.removeAllItems();
    do{
        jComboBoxMedicos.addItem(conecte.rs.getString("nome"));
    }while(conecte.rs.first());
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro ao preencher o campo medico"+ex);
        }
    conecte.desconecta();
}

