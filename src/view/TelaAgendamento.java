    public void atualizarTabela(String SQL){
        //dados = new ArrayList(); 
        ArrayList dados = new ArrayList();      
        String[] Colunas = new String[]{"ID","Nome","Idade", "CPF", "Telefone","Bairro"};//nome das colunas
        
        conecte.conexao();
        conecte.executaSql(SQL);
        
        try {
            conecte.rs.first();
             do{
                 dados.add(new Object[]{
                     
                     conecte.rs.getString("id_pac"),
                     conecte.rs.getString("nome_pac"),//pega do banco
                     conecte.rs.getString("idade_pac"),
                     conecte.rs.getString("cpf_pac"),
                     conecte.rs.getString("telefone_pac"), 
                     conecte.rs.getString("bairro")
                     
                 });
            }while(conecte.rs.next());//quando passar para o proximo para
             
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Verifique se escreveu corretamente"+ex);
        }
        
        Table tab = new Table(dados, Colunas);

        jTablePaciente.setModel(tab);
        jTablePaciente.getColumnModel().getColumn(0).setPreferredWidth(40);
        jTablePaciente.getColumnModel().getColumn(0).setResizable(false);
        jTablePaciente.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTablePaciente.getColumnModel().getColumn(1).setResizable(false);
        jTablePaciente.getColumnModel().getColumn(2).setPreferredWidth(60);
        jTablePaciente.getColumnModel().getColumn(2).setResizable(false);
        jTablePaciente.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTablePaciente.getColumnModel().getColumn(3).setResizable(false);
        jTablePaciente.getColumnModel().getColumn(4).setPreferredWidth(130);
        jTablePaciente.getColumnModel().getColumn(4).setResizable(false);
        jTablePaciente.getColumnModel().getColumn(5).setPreferredWidth(150);
        jTablePaciente.getColumnModel().getColumn(5).setResizable(false);


        jTablePaciente.getTableHeader().setReorderingAllowed(false);//não vai poder reorganizar as ordem das colunas
        jTablePaciente.setAutoResizeMode(jTablePaciente.AUTO_RESIZE_OFF);//O usuario não vai poder modificar o tamanho da tabela
        jTablePaciente.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//so vai poder selecionar um dado por vez

        conecte.desconecta();

    }
    
