           mod.setBairro(jTextBairro.getText());
           mod.setCidade(jTextCidade.getText());

           dao.Salvar(mod);

           jTextiD.setText("");
           jTextBairro.setText("");
           jTextLogradouro.setText("");//limpa os campos
           jTextCasa.setText("");
           jTextCidade.setText("");
        
           jButtonPaciente.setEnabled(true);
           jTextLogradouro.setEnabled(false);
           jTextCasa.setEnabled(false);
           jTextCidade.setEnabled(false);//desativa os campos
           jTextBairro.setEnabled(false);
        
           jButtonSalvar.setEnabled(false);
           jButtonCancelar.setEnabled(false);

           jButtonNovo.setEnabled(true);
        
