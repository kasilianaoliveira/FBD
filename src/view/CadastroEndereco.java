    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        String nomeUser = ""+jTable1.getValueAt(jTable1.getSelectedRow(),1);//oq for selecionado e vai colocar na string
        conecte.conexao();
        conecte.executaSql("select *from endereco where logradouro ='"+nomeUser+"'");
        try {
 
            if(conecte.rs.first()){//vai pegar o primeiro clique e vai verificar se é depois verifcar no banco se realmente tem esses dados
                
                jTextiD.setText(String.valueOf(conecte.rs.getInt("id_endereco")));
                jTextLogradouro.setText(conecte.rs.getString("logradouro"));
                jTextCasa.setText(String.valueOf(conecte.rs.getInt("num_casa")));
                jTextBairro.setText(conecte.rs.getString("bairro"));
                jTextCidade.setText(conecte.rs.getString("cidade"));
                //jPasswordConfirme.setText(conecte.rs.getString("login_id"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecioonar o dado"+ex);//caso de erro aparece uma mensagem
        }
        jButtonNovo.setEnabled(false);
        jButtonAlterar.setEnabled(true);//habilita botoes
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        jButtonSalvar.setEnabled(false);
        conecte.desconecta();
    }//GEN-LAST:event_jTable1MouseClicked

    public void atualizarTabela(String SQL){
        //dados = new ArrayList(); 
        ArrayList dados = new ArrayList();      
        String[] Colunas = new String[]{"ID","Logradouro","Nº da casa", "Bairro", "Cidade"};//nome das colunas
        
        conecte.conexao();
        conecte.executaSql(SQL);
        
        try {
            conecte.rs.first();
             do{
                 dados.add(new Object[]{
                     
                     conecte.rs.getString("id_endereco"),
                     conecte.rs.getString("logradouro"),//pega do banco
                     conecte.rs.getString("num_casa"),
                     conecte.rs.getString("bairro"),
                     conecte.rs.getString("cidade")
                     
                 });
            }while(conecte.rs.next());//quando passar para o proximo para
             
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Verifique se escreveu corretamente");
        }
        
        Table tab = new Table(dados, Colunas);

        jTable1.setModel(tab);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTable1.getColumnModel().getColumn(0).setResizable(false);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTable1.getColumnModel().getColumn(1).setResizable(false);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(2).setResizable(false);
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(170);
        jTable1.getColumnModel().getColumn(3).setResizable(false);
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(180);
        jTable1.getColumnModel().getColumn(4).setResizable(false);

        jTable1.getTableHeader().setReorderingAllowed(false);//não vai poder reorganizar as ordem das colunas
        jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_OFF);//O usuario não vai poder modificar o tamanho da tabela
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//so vai poder selecionar um dado por vez

        conecte.desconecta();

    }
    
    
