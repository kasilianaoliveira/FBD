    private void preencherNome(){
        conecte.conexao();
        conecte.executaSql("select *from endereco order by bairro");
        try {
            conecte.rs.first();
            jComboBoxBairro.removeAllItems();
            do{
               jComboBoxBairro.addItem(conecte.rs.getString("bairro"));
            }while(conecte.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher bairro"+ex);
        }
        conecte.desconecta();
    }
