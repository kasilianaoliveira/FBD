    private void jTablePacMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePacMouseClicked
        String nomePac = ""+jTablePac.getValueAt(jTablePac.getSelectedRow(),1);//oq for selecionado e vai colocar na string
        conecte.conexao();
        conecte.executaSql("select *from paciente where nome_pac ='"+nomePac+"'");
        try {
 
            if(conecte.rs.first()){//vai pegar o primeiro clique e vai v erificar se é depois verifcar no banco se realmente tem esses dados
                
                jTextFieldID.setText(String.valueOf(conecte.rs.getInt("id_pac")));
                jTextNome.setText(conecte.rs.getString("nome_pac"));
                jTextFieldIdade.setText(String.valueOf(conecte.rs.getInt("idade_pac")));
                jFormattedCpf.setText(conecte.rs.getString("cpf_pac"));
                jFormattedTextTelefone.setText(conecte.rs.getString("telefone_pac"));
                jComboBoxSexo.setSelectedItem(conecte.rs.getString("sexo_pac"));
                jTextFieldIdade.setText(String.valueOf(conecte.rs.getInt("idade_pac")));
                
                //quando se tem chave estrangeira é necessario fazer uma nova conexao selecionando a table dessa chave
                ConexaoBanco conexaoPesquisa = new ConexaoBanco();
                conexaoPesquisa.conexao();
                conexaoPesquisa.executaSql("select *from endereco where id_endereco="+conecte.rs.getInt("id_end"));
                conexaoPesquisa.rs.first();
                jComboBoxBairro.setSelectedItem(conexaoPesquisa.rs.getString("bairro"));
                conexaoPesquisa.desconecta();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar o dado"+ex);//caso de erro aparece uma mensagem
        }
        jButtonNovo.setEnabled(false);
        jButtonAlterar.setEnabled(true);//habilita botoes
        jButtonExcluir.setEnabled(true); 
        jButtonCancelar.setEnabled(true);
        jButtonSalvar.setEnabled(false);
        jTextFieldID.setEnabled(false);
        jTextNome.setEnabled(false);
        /*jPassword.setEnabled(false); 
        jPasswordConfirme.setEnabled(false);
        jComboBox1.setEnabled(false);*/

    }//GEN-LAST:event_jTablePacMouseClicked

