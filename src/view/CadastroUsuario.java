    private void jTableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUserMouseClicked
        String nomeUser = ""+jTableUser.getValueAt(jTableUser.getSelectedRow(),1);//oq for selecionado e vai colocar na string
        conecte.conexao();
        conecte.executaSql("select *from caduser where nome_user ='"+nomeUser+"'");
        try {
 
            if(conecte.rs.first()){//vai pegar o primeiro clique e vai verificar se √© depois verifcar no banco se realmente tem esses dados
                
                jTextFieldID.setText(String.valueOf(conecte.rs.getInt("id_user")));
                jTextNome.setText(conecte.rs.getString("nome_user"));
                jFormattedCpf.setText(conecte.rs.getString("cpf"));
                jComboBox1.setSelectedItem(conecte.rs.getString("tipo"));
                jPassword.setText(conecte.rs.getString("senha"));
                jPasswordConfirme.setText(conecte.rs.getString("senha"));
                //jPasswordConfirme.setText(conecte.rs.getString("login_id"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar o dado"+ex);//caso de erro aparece uma mensagem
        }
        jButtonNovo.setEnabled(false);
        jButtonAlterar.setEnabled(true);//habilita botoes
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        jButtonSalvar.setEnabled(false);
        jTextFieldID.setEnabled(false);
        jTextNome.setEnabled(false);
        jPassword.setEnabled(false); 
        jPasswordConfirme.setEnabled(false);
        jComboBox1.setEnabled(false);
        conecte.desconecta();
    }//GEN-LAST:event_jTableUserMouseClicked

