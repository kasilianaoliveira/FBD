    public void atualizarTabela(String SQL){
        //dados = new ArrayList(); 
        ArrayList dados = new ArrayList();      
        String[] Colunas = new String[]{"ID","Nome","CPF", "Tipo"};//nome das colunas
        
        conecte.conexao();
        conecte.executaSql(SQL);
        
        try {
            conecte.rs.first();
             do{
                 dados.add(new Object[]{
                     
                     conecte.rs.getString("id_user"),
                     conecte.rs.getString("nome_user"),//pega do banco
                     conecte.rs.getString("cpf"),
                     conecte.rs.getString("tipo")
                     
                 });
            }while(conecte.rs.next());//quando passar para o proximo para
             
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null,"Verifique se escreveu corretamente");
        }
        
        Table tab = new Table(dados, Colunas);

        jTableUser.setModel(tab);
        jTableUser.getColumnModel().getColumn(0).setPreferredWidth(230);
        jTableUser.getColumnModel().getColumn(0).setResizable(false);
        jTableUser.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTableUser.getColumnModel().getColumn(1).setResizable(false);
        jTableUser.getColumnModel().getColumn(2).setPreferredWidth(135);
        jTableUser.getColumnModel().getColumn(2).setResizable(false);
        jTableUser.getColumnModel().getColumn(3).setPreferredWidth(170);
        jTableUser.getColumnModel().getColumn(3).setResizable(false);

        jTableUser.getTableHeader().setReorderingAllowed(false);//n√£o vai poder reorganizar as ordem das colunas
        jTableUser.setAutoResizeMode(jTableUser.AUTO_RESIZE_OFF);//O usuario n√£o vai poder modificar o tamanho da tabela
        jTableUser.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//so vai poder selecionar um dado por vez

        conecte.desconecta();

    }
    
    
